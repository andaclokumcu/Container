using System;

namespace Loop__for_
{
    class Program
    {
        static void Main(string[] args)
        {
            for (int i = 0; i < 10; i++)
            {
                Console.WriteLine(i);
            }
            // i = 1'den başla, i 10'dan küçük kalana kadar 10'a kadar arttırarak devam et.
        }
    }
}
